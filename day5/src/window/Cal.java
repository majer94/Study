package window;

public class Cal {
	int		res;
	
	public	int	add(int	x, int y) {
		res	= x + y;
		return	res;
	}
	
	public	int	sub(int	x, int y) {
		res	= x - y;
		return	res;
	}
	
	public	int	mul(int	x, int y) {
		res	= x * y;
		return	res;
	}
	
	public	int div(int	x, int y) {
		res	= x / y;
		return	res;
	}

}
